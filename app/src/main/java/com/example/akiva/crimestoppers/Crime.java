
public class Crime {
	String offense;
	String address;
	double xCoordinate;
	double yCoordinate;
	
	Crime (String offense, String address, double xCoordinate, double yCoordinate) {
		this.offense = offense;
		this.address = address;
		this.xCoordinate = xCoordinate;
		this.yCoordinate = yCoordinate;
	}
}
